# 복합 연산자
a = 1
a = a + 1
print(a)

a = 1
a += 1
print(a)



a = 1
a = a - 1
print(a)

a = 1
a -= 1
print(a)



# 복합 연산자의 종류
# +=, -=, *=, /=, //=, %=, **=

b = 2
print(b)
b += 2
print(b)
b -= 2
print(b)
b *= 2
print(b)
b /= 2
print(b)
b **= 2
print(b)
b //= 2
print(b)
b %= 2
print(b)


# 변수 c에 5를 가지고 복합 연산자 +=, -=, *=, /=, **=, %=, //=를 사용해서 출력해주세요.

c = 5
c += 5
print(c) # 10
c -= 5
print(c) # 5
c *= 5
print(c) # 25
c /= 5
print(c) # 5
c **= 5
print(c) # 3125
c %= 5
print(c) # 0
c //= 5
print(c) # 0


